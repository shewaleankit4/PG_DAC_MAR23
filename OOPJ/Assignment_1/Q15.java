public class Q15 {
    
    public static void main(String[] args) {
        Integer I = new Integer("3456");
        System.out.println("INT instance into byte "+I.byteValue());
        System.out.println("INT instance into int "+I.shortValue());
        System.out.println("INT instance into float  "+I.floatValue());
        System.out.println("INT instance into double"+I.doubleValue());
        System.out.println("INT instance into long "+I.longValue());
    }
}
